/**
 * School
 */
public class School {
    
    //Field
    private String name;
    private String address;
    
    //Constructor
    public School(){}

    //Method Set Name
    public void setName(String name){
        this.name = name;
    }

    //Method Get Name
    public String getName(){
        return this.name;
    }

    //Method Set Address
    public void setAddress(String address){
        this.address = address;
    }

    //Method Get Address
    public String getAddress(){
        return this.address;
    }
}