/**
 * Classroom
 */
public class Classroom {

    //Field
    private String level;
    private String name;

    //Constructor
    public Classroom(){}

    //Method Set Level
    public void setLevel(String level){
        this.level = level;
    }

    //Method Get Level
    public String getLevel(){
        return this.level;
    }

    //Method Set Name
    public void setName(String name){
        this.name = name;
    }

    //Method Get Name
    public String getName(){
        return this.name;
    }
}
